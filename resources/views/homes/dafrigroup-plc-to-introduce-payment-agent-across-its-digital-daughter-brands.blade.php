                        <h3>How does Payment Agent work?</h3>
                        
                        <p>Payment Agent (PA) is a practice used by most brokerage firms to offer service to people without a Visa/Master card privilege. Or those residing in countries restricted from participating in the digital economic opportunities.</p>
                        
                        <p>Platform owner create a portal that allow an Authorized Payment Agent to send money to other user at no extra cost.</p>
                        
                        <p>Platform owner allow the Authorized Payment Agent to charge commission between 1%- 9% per transaction, albeit not greater than 9%.</p>
                        
                        <p>The authorized Payment Agent is required to have a minimum capital set by the platform owner and meet various criteria to begin operating as an official Payment Agent. Once approved, the Authorized Payment Agent will act as a middle person between the platform owner and its clients. The authorized payment agent will be able to collect deposits from the platform clients residing in country's of he/her operation.</p>
                        
                        <p>Deposit can be picked up through over the counter cash payment, bank transfer, mobile money, eWallet, Bitcoin, DBA, Ethereum depending on the authorized Payment Agent preference. Upon confirmation of payment, the Authorized Payment agent will transfer the the agreed amount to client's platform back office account after deduction of commission.</p>
                        
                        <p>The model is also adopted on withdrawals where clients transfer to the authorized payment agent back office previously used for deposit and receive payout in their respective country of residence.</p>
                        
                        <p>Payment agent is one of the biggest crypto adoption lead since its based on P2P decentralized matrix which can be carried out by over the counter cash exchange without the requirement of bank account.</p>
                        
