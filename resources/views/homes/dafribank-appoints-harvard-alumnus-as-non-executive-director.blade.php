                        <p>DafriBank PLC is pleased to announce the appointment of Dr. Edward Obasi in the role of Non-Executive Director with effect from 1st November 2020. This marks the beginning of an exciting relationship for the rapidly evolving digital-only bank. Dr Obasi brings to DafriBank more than 20 years of professional experience with a proven track record in the banking sector as he now becomes a pivotal member of the team, in joining the company's Board of Directors.</p>
                        
                        <p>He has served at various financial institutions, including Ecobank Nigeria PLC, Intercontinental Bank PLC, Hallmark Bank PLC, Savannah Bank PLC amongst others. His areas of competence include: Wealth and Credit Risk Management, Commercial Banking, Human Resource Management, Internal Controls, Performance Management, and Business Process Improvements. Prior to joining DafriBank, Dr Obasi held various important positions in many other industries including Chief Accountant, Senior Financial analyst, Treasury Manager with top organizations in Canada and United States of America.</p>
                        
                        <p>Dr Obasi is also vastly educated. He is an alumnus of the prestigious Harvard University Business School. He has a Bachelor's degree in finance and banking and master’s degree in business administration (MBA) amongst several other professional certifications and memberships including: Fellow Chartered Certified Accountants of UK (FCCA), Certified Professional Accountant of Alberta and Canada (CPA, CGA), Associate Chartered Institute of Bankers of Nigeria (ACIB). Obasi has a Honorary doctorate in Business Administration from Pebble Hills University Delaware, U.S.A.<br>With him based in Canada, his new appointment will see him lead the delivery of DafriBank's ambitious growth plans on a global scale.</p>
                        
                        <p>Commenting on the appointment, Mr. Xolane Ndhlovu, Chairman of DafriGroup PLC: “I am beyond thrilled to welcome Dr Obasi to the DafriGroup family. His global experience in driving significant business growth, his knowledge of the banking industry and his management expertise will be invaluable as we continue to strengthen and grow DafriBank”.</p>
                        
                        <p>Commenting on his appointment, Obasi said: “I am extremely honoured to be joining DafriBank at such a digital, rapidly evolving and exciting time in the world. I am deeply passionate about the banking industry and fully aware of how modern digital solutions are streamlining operations in this industry. I look forward to contributing my perspective and experience towards actualizing DafriGroup PLC overall corporate goals.</p>
                        
